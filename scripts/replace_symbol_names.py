# Sort by SID descending (longest first) to avoid partial matches
sids = sorted(mapping.keys(), key=lambda x: -int(x[1:]))

def replace_file(filepath):
    with open(filepath) as f:
        content = f.read()
    
    original = content
    for sid in sids:
        real_name = mapping[sid]
        # Replace "$N" -> "real_name" (complete string literals only)
        content = content.replace(f'"{sid}"', f'"{real_name}"')
    
    if content != original:
        with open(filepath, 'w') as f:
            f.write(content)
        # Count changes
        changes = sum(1 for a, b in zip(original.split('\n'), content.split('\n')) if a != b)
        print(f'  {filepath}: {changes} lines changed')
        return True
    return False

